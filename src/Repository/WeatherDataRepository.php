    /**
     * @throws ORMException
     * @throws OptimisticLockException
     */
    public function add(WeatherData $entity, bool $flush = true): void
    {
        $this->_em->persist($entity);
        if ($flush) {
            $this->_em->flush();
        }
    }

    /**
     * @throws ORMException
     * @throws OptimisticLockException
